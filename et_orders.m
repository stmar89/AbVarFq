crZQ:=function(M)
//given a matrix M over the Integers() changes the base ring to Rationals()
    return ChangeRing(M,Rationals());
end function;

crQZ:=function(M)
//given a matrix M over the Rationals() changes the base ring to Integers()
    return ChangeRing(M,Integers());
end function;

Columns:=function(M)
    return Rows(Transpose(M));
end function;

hnf:=function(M)
//returns a square matrix in hnf
    N:=Rank(M);
    H:=Matrix( Rows(HermiteForm(M))[1..N] );
    return ChangeRing(H,Integers());
end function;

MatrixQ:=function(gens)
//return a matrix with rational entries which rows the elements of gens
    Ass:=AssAlgebra(Universe(gens));
    return Matrix([Eltseq(Ass!g) : g in gens]);
end function;

MatrixZ:=function(gens)
//return a matrix with integers entries which rows the elements of gens
    return crQZ(MatrixQ(gens));
end function;

MatrixToA:=function(A,P)
    return [A!(AssAlgebra(A)!Eltseq(r)) : r in Rows(P)];
end function;

