intrinsic IsCoercible(S::AlgEtOrd, x::.) -> BoolElt, Any
{Return whether x is coercible into S and the result if so}
    bool,x:=IsCoercible(Algebra(S),x);
    if bool and x in S then
        return true,x;
    else
        return false,_;
    end if;
end intrinsic;

