intrinsic Order(gens::SeqEnum[AlgEtElt]) -> AlgEtOrd
{construnct the order generated by gens}
  AEt:=Universe(gens);
  A := AssAlgebra(AEt);
  F := BaseRing(A);
  Z_F:=Integers(F); 
  n := Dimension(A);
  S:=[x`AlgAssElt : x in gens];
  I:=[ideal<Z_F | 1> : i in [1..#S]];
  if (A!1 notin S) then
    Append(~S, 1);
    Append(~I, 1*Z_F);
  end if;
  // Find an initial pseudobasis. not changed
  M := Matrix(F,#S,n, &cat[Eltseq(s) : s in S]);
  P := PseudoMatrix(I,M);
  P := HermiteForm(P);
  I := CoefficientIdeals(P);
  M := ChangeRing(Matrix(P),F);
  S := [A ! Eltseq(M[i]) : i in [1..Nrows(M)]];

