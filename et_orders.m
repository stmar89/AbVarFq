intrinsic Order(A::AlgEt,O:AlgAssVOrd)->AlgEtOrd
{creates and etale order from the underlying order in the associative algebra}
    require AssAlgebra(A) cmpeq Algebra(O) : "the underlying associateive algebra must be the same";
    R:=New(AlgEtOrd);
    R`Order:=O;
    R`Algebra:=A;
    R`ZBasis:=[A ! z : z in ZBasis(O)];
    return R;
end intrinsic;

