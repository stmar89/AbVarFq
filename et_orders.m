intrinsic Algebra(S::AlgEtOrd) -> AlgEt
{returns the algebra of the order}
  return S`Algebra;
end intrinsic;

intrinsic AssociativeOrder(O:AlgEtOrd)->AlgAssVOrd
{return the underlying associative order}
    if not assigned O`Order then
        A:=Algebra(O);
        O`Order:=Order([AssAlgebra(A)!g : g in Generators(O)]);
    end if;
    return O`Order;
end intrinsic;

intrinsic AssOrder(O:AlgEtOrd)->AlgAssVOrd
{return the underlying associative order}
    return AssociativeOrder(O);
end intrinsic;

