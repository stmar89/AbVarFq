intrinsic ZBasis(S::AlgEtOrd)->SeqEnum[AlgEtElt]
{return a Z-basis of the order}    
    if not assigned S`ZBasis then
        A:=Algebra(S);
        zb:=ZBasis(AssOrder(S));
        S`ZBasis:=[A!z : z in zb];
    end if;
    return S`ZBasis;
end intrinsic;

intrinsic Generators(S::AlgEtOrd)->SeqEnum[AlgEtElt]
{return a set of generators of the order}    
    if not assigned S`Generators then
        S`Generators:=ZBasis(S);
    end if;
    return S`Generators;
end intrinsic;

